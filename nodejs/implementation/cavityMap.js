// Complete the cavityMap function below.
function cavityMap(grid) {

    for(let i = 1; i < grid.length - 1; i++){
        for(let j = 1; j < grid.length - 1; j++){
            
            let isCavity = true;
            for(let line = i - 1; line <= i + 1; line++){
                for(let column = j - 1; column <= j + 1; column++){
                    if(i !== line || j !== column){
                        let current = parseInt(grid[i][j], 10);
                        let adjacent = parseInt(grid[line][column], 10);
                        if(current < adjacent){
                            isCavity = false;
                        }
                    }
                }
            }
            if(isCavity){
                console.log('change')
                grid[i] = grid[i].substr(0, j) + 'X' + grid[i].substr(j + 1);
            }
        }
    }

    return grid;
}

